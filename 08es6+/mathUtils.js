/**
 * Q23 — Named exports.
 * `export` in front of each function makes it available to any other file
 * that imports from this one, by that exact name.
 */

export function add(a, b) {
  return a + b;
}

export function subtract(a, b) {
  return a - b;
}

export function multiply(a, b) {
  return a * b;
}
